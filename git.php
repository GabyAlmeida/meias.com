Git é um progama de versionamento de código
github é um servidor na nuvem que armazana seus códigos 

na prática

criar novo repositorio no git

git config --global user.name GabyAlmeida
git config --global user.email gabysalmeida9182@gmail.com
git init
git clone https://github.com/GabyAlmeida/meias.com.git
cole seu projeto na pasta
no terminal
entre na pasta
git add *
git commit -m 'nova pasta'
git push